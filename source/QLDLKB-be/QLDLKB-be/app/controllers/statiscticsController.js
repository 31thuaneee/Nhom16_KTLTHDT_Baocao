const Statistics = require("../models/statisticsModel");
const model = new Statistics();

exports.getCustomer = function(req, res) {
    model.getCustomer(function (status, data, message, error) {
        res.send({
            status: status,
            data: data,
            message: message,
            error: error
        })
    }).then(value => {
        console.log(value);
    });
};

